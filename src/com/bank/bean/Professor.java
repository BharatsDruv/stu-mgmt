package com.bank.bean;

public class Professor {
	
	private int professor_id;
	public int getProfessor_id() {
		return professor_id;
	}
	public void setProfessor_id(int professor_id) {
		this.professor_id = professor_id;
	}
	public String getProfessor_idame() {
		return professor_idame;
	}
	public void setProfessor_idame(String professor_idame) {
		this.professor_idame = professor_idame;
	}
	public String getProfessor_course_id() {
		return professor_course_id;
	}
	public void setProfessor_course_id(String professor_course_id) {
		this.professor_course_id = professor_course_id;
	}
	public String getProfessor_username() {
		return professor_username;
	}
	public void setProfessor_username(String professor_username) {
		this.professor_username = professor_username;
	}
	public String getProfessor_password() {
		return professor_password;
	}
	public void setProfessor_password(String professor_password) {
		this.professor_password = professor_password;
	}
	public String getProfessor_email() {
		return professor_email;
	}
	public void setProfessor_email(String professor_email) {
		this.professor_email = professor_email;
	}
	private String professor_idame;
	private String professor_course_id;
	private String professor_username;
	private String professor_password;
	private String professor_email;
	
	
}
